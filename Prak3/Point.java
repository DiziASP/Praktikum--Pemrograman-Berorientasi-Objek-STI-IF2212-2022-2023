public class Point {
    private int x;
    private int y;

    public Point(int a, int b) {
        this.x = a;
        this.y = b;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}